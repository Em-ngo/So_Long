#include "so_long.h"

int	all_map(t_all *all)
{
	int	x;
	int	y;
	int	i;
	int	j;

	y = 0;
	i = 0;
	x = 0;
	j = 0;
	while (y < 200)
	{
		while (x < 200)
		{
			put_img_on_window(all, x, y, all->map[i][j]);
			x += 50;
			j++;
		}
		j = 0;
		x = 0;
		y += 50;
		i++;
	}
	return (0);
}